    public void borro() {
        Codigo.setText("");
        Nombre.setText("");
        Producto.setText("");
        Precio.setText("");
    }

    public void lala() {
        Codigo.setText(String.valueOf(Tabla.getValueAt(Tabla.getSelectedRow(), 0)));
        Nombre.setText(String.valueOf(Tabla.getValueAt(Tabla.getSelectedRow(), 1)));
        Producto.setText(String.valueOf(Tabla.getValueAt(Tabla.getSelectedRow(), 2)));
        Precio.setText(String.valueOf(Tabla.getValueAt(Tabla.getSelectedRow(), 3)));
    }

    public void Filtro() {
        TableRowSorter<DefaultTableModel> Sorter;
        DefaultTableModel Modelo = (DefaultTableModel) this.Tabla.getModel();
        Sorter = new TableRowSorter<>(Modelo);
        Tabla.setRowSorter(Sorter);
        Sorter.setRowFilter(RowFilter.regexFilter(FIL.getText(), 1));

    }

