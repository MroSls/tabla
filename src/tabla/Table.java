    private void AgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AgregarActionPerformed
        if (Codigo.getText().isEmpty() || Nombre.getText().isEmpty() || Producto.getText().isEmpty() || Precio.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "No haz llenado los campos");
        } else {
            DefaultTableModel Modelo = (DefaultTableModel) this.Tabla.getModel();
            String codigo = Codigo.getText(), nombre = Nombre.getText(), producto = Producto.getText(), precio = Precio.getText();
            Modelo.addRow(new Object[]{codigo, nombre, producto, precio});
            this.Tabla.setModel(Modelo);
            borro();
        }
    }//GEN-LAST:event_AgregarActionPerformed

    private void BorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BorrarActionPerformed
        DefaultTableModel Modelo = (DefaultTableModel) this.Tabla.getModel();
        if (Tabla.getSelectedRow() < 0) {
            JOptionPane.showMessageDialog(null, "Debe de seleccionar de fila");
        } else {
            Modelo.removeRow(Tabla.getSelectedRow());
            Tabla.setModel(Modelo);
        }
    }//GEN-LAST:event_BorrarActionPerformed

